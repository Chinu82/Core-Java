class Wrapper{
    public static void main(String[] args) {
        //Using datatype
        int a = 10;
        int b = 20;
        System.out.println("Addition = "+(a+b));
        //Without using datatype or, Using Wrapper class
        Integer i = 10;
        Integer j = 20;
        System.out.println("Addition = "+(i+j));
    }
}
